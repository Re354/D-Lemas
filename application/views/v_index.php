    <p align='justify'> Pada penelitian Implementasi Model Prototipe Pada Digital Learning Management System
        (Di-Lemas). Metode digital learning memberikan kemudahan dan kelancaran proses belajarmengajar bagi siswa dan guru. Dengan metode digital learning, guru dapat meningkatkan
        intensitas komunikasi interaktif dengan siswa di luar jam kelas resmi. Metode digital learning
        memberikan fleksibilitas kepada pengajar untuk memberikan akses kepada siswa untuk
        mendapatkan referensi ilmiah yang berkaitan dengan pelajaran yang mungkin tidak diperoleh
        selama jam sekolah dan kegiatan praktikum. Referensi ini bisa berupa makalah ilmiah, artikel
        populer, atau buku elektronik. Hal ini akan sangat berguna bagi siswa, karena selain
        meningkatkan pemahaman siswa pada setiap mata pelajaran, referensi metode pembelajaran
        melalui media elektronik dapat menjadi salah satu cara menarik perhatian siswa sehingga
        pembelajaran menjadi flexibel, dinamis, kompeten dan antisipatif sesuai dengan misi SMK
        Pancakarya Tangerang. Harapannya adalah lulusan SMK Pancakarya Tangerang, akan
        memiliki kualitas dan daya saing yang tinggi dalam persaingan global terutama dalam bidang
        Ilmu Pengetahuan dan Teknologi (IPTEK). Sesuai tahapan dalam mengembangkan Digital
        Learning Management System(Di-LeMas) secara detail diuraikan berdasarkan analisa sistem,
        berbagai fungsional yang dibutuhkan oleh sistem sebagai berikut: Kebutuhan Security
        System, Kebutuhan User, Desain dan Implementasi user Interface.
